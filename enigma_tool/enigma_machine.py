# 3 Rotoren

# Rotor 1
    # 26 pins vorne
    # 26 pins hinten
    # pin vorne 1 --> pin hinten 6,
    # pin vorne 2 --> pin hinten 7,
    # pin vorne 3 --> pin hinten 8 ... ... ...
    # pro Eingabe dreht sie sich

# Rotor 2
    # 26 pins vorne
    # 26 pins hinten
    # pin vorne 1 --> pin hinten 6,
    # pin vorne 2 --> pin hinten 7,
    # pin vorne 3 --> pin hinten 8 ... ... ...
    # nach z. B. 25 Eingaben dreht Rotor 1 sie einen Schritt weiter

# Rotor 3
    # 26 pins vorne
    # 26 pins hinten
    # pin vorne 1 --> pin hinten 6,
    # pin vorne 2 --> pin hinten 7,
    # pin vorne 3 --> pin hinten 8 ... ... ...
    # nach z. B. 250 Eingaben (Rotor 1 - 25 Umdrehungen * Rotor 2 - 10 Umdrehungen) dreht sie sich einen Schritt weiter


